#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		len;
	int		i;
	int		j;
	char	*result;

	i = 0;
	j = 0;
	len = 0;
	len = ft_strlen(s1);
	len += ft_strlen(s2);
	result = malloc((sizeof (char) * len) + 1);
	if (result == NULL)
		return (NULL);
	while (s1[i])
		result[j++] = s1[i++];
	i = 0;
	while (s2[i])
		result[j++] = s2[i++];
	result[j] = '\0';
	return (result);
}
